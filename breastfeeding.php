<h1>
	Breastfeeding: Getting Off to a Great Start
</h1>

<ul>

	<li>
		Tell your doctor and nurses in the hospital that you plan to breastfeed and not to give any bottles or 
		pacifiers to your baby. 
	</li>
<br>
	<li> 
		Breastfeed your baby as soon as possible after delivery. Tell your doctor and nurses that you want to 
		hold your baby skin-to-skin during the first hour or two after birth. 
	</li>
<br>
	<li> 
		Ask the hospital if they have a lactation consultant who can give you helpful advice. 
	</li>
<br>
	<li> 
		Breastfeeding takes practice—just like learning a new dance. It gets easier as your baby grows. 
	</li>
<br>
	<li> 
		Try some of the positions on the next pages. Relax while you and your baby get used to breastfeeding. 
	</li>
<br>
	<li> 
		Call your WIC office if you need help with breastfeeding.
	</li>

</ul>

<h2>
	Finding Comfortable Positions
</h2>
<p>The best way to help your baby use his natural instincts.</p>
<ul>
<li>Lean back comfortably; use pillows for support, if you want.</li>
<br>
<li>Have your baby skin-to-skin, or in light clothing (not wrapped). </li>
<br>
<li>Lay baby on your chest; let him move towards your breasts. Relax and enjoy. </li>
<br>
<li>Your body supports your baby’s body; use pillows or blankets to help. </li>
</ul>

<h2>Cross Cradle</h2>
<p>Good for smaller babies.</p>
<ul>
<li>Support your baby’s body and neck.</li>
<br>
<li>Put your baby across your body tummy-to-tummy with you.</li>
<br>
<li>Wrap her legs around your side. </li>
<br>
<li>Baby breastfeeds on the breast opposite your supporting arm.</li>
</ul>


