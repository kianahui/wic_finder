<h2>
	Selecting Whole Grain Foods
</h2>
<p>Look for food with a whole grain listed as the first ingredient. A food has more of the first ingredient 
	than any other ingredient.</p>

	<h2>Here are a few ways to eat more fruits and vegetables every day!</h2>

	<ul>

		<li>Keep cut up fruits and vegetables ready for a quick salad.</li>
		<br>
		<li>Cut up raw vegetables like carrots, celery, jicama, cucumber, peppers, and zucchini and store in the 
			refrigerator for an easy snack. </li>
			<br>
			<li>Add a colorful variety of vegetables to soups, casseroles, stews, rice dishes and spaghetti sauces.</li>
			<br>
			<li>Add raw, sauteed, or roasted vegetables to scrambled eggs, quesadillas, burritos, sandwiches, or 
				wraps. </li>
				<br>
				<li>Keep fresh fruit in easy reach for a quick snack. </li>
				<br>
				<li>Use sliced or chopped fruit as a topping for pancakes, waffles, and cereal.</li>
				<br>
				<li>Add chopped or mashed fruit or berries to muffins, cake, and cookies. </li>

	</ul>

